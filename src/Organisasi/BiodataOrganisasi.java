    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        int reply = JOptionPane.showConfirmDialog(null, "Apakah Anda ingin menyimpan data tersebut?");
        if(rdlaki.isSelected()){
            jenkel = "Laki - Laki";
        } else if(rdperempuan.isSelected()){
            jenkel = "Perempuan";
        }
        String JenisPekerjaan = cmbpekerjaan.getSelectedItem().toString();
        if(reply == JOptionPane.YES_OPTION){
           try{
            BufferedWriter out = new BufferedWriter(
                    new FileWriter("BiodataMahasiswa.txt"));
            out.write("Nama : \t" + textnama.getText() + "\n Jenis Kelamin :\t" 
                + jenkel + "\n Jenis Pekerjaan :\t"+JenisPekerjaan +"\n Alamat : \t" + textalamat.getText());
            out.close();
            } catch (Exception e){
            System.out.println("Error boss, check codingannya!");
            }
        }  
    }//GEN-LAST:event_simpanActionPerformed

    private void chkcetaktebalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkcetaktebalActionPerformed
        // TODO add your handling code here:
        Font tebal = new Font("Arial",Font.BOLD,12);
        Font tipis = new Font("Arial",Font.ITALIC,12);
        
        if(chkcetaktebal.getText().equals("Ya")){
            textarea.setFont(tebal);
        } else{
            textarea.setFont(tipis);
        }
    }//GEN-LAST:event_chkcetaktebalActionPerformed

