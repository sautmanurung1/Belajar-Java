    private void tampilkanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tampilkanActionPerformed
        // TODO add your handling code here:
        if(rdlaki.isSelected()){
            jenkel = "Laki - Laki";
        } else if(rdperempuan.isSelected()){
            jenkel = "Perempuan";
            buttonGroup1.clearSelection();
        }
        String JenisPekerjaan = cmbpekerjaan.getSelectedItem().toString();
        textarea.setText("Nama : \t" + textnama.getText() + "\n Jenis Kelamin :\t" 
                + jenkel + "\n Jenis Pekerjaan :\t"+JenisPekerjaan +"\n Alamat : \t" + textalamat.getText());
        
    }//GEN-LAST:event_tampilkanActionPerformed

